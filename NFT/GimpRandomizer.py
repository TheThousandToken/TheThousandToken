def word_number(number):
  if number>=0 and number<=1000:
    a = ['','First','Second','Third','Fourth','Fifth','Sixth','Seventh','Eighth','Ninth','Tenth','Eleventh','Twelfth','Thirteenth','Fourteenth','Fifteenth','Sixteenth','Seventeenth','Eighteenth','Nineteenth','Twenty-','Thirty-','Forty-','Fifty-','Sixty-','Seventy-','Eighty-','Ninety-']
    c = ['','One','Two','Three','Four','Five','Six','Seven','Eight','Nine','Tenth','eleven','twelve','thirteen','fourteen','fifteen','sixteen','seventeen','eighteen','nineteen','Twentieth','Thirtieth','Fortieth','Fiftieth','Sixtieth','Seventieth','Eightieth','Nintieth']
    if number<=20:
      if number%10==0: return c[number]
      else: return a[number]
    elif number<100:
      b=number-20
      r=b%10
      b//=10
      if number%10==0: return c[20+b]+a[r]
      else: return a[20+b]+a[r]
    elif number<1000:
      if number%100==0:
        b=number//100
        return c[b]+' Hundredth'
      else:
        r=number%100
        b=number//100
        if r<=20:
          if number%10==0: return c[b]+' Hundred '+ c[r]
          else: return c[b]+' Hundred '+a[r]
        else:
          r=r-20
          d=r//10
          r%=10
          if number%10==0: return c[b]+' Hundred '+ c[20+d]
          else: return c[b]+' Hundred '+a[20+d]+a[r]
    elif number==1000:
      return 'One Thousandth'
    else:
      return -1

